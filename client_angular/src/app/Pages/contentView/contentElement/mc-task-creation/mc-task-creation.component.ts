import { catchError } from 'rxjs/operators';
interface Answer {
  answer?: string;
  correct?: boolean;
}
@Component({
  selector: 'app-mc-task-creation',
  templateUrl: './mc-task-creation.component.html',
  styleUrls: ['./mc-task-creation.component.scss'],
})
export class McTaskCreationComponent implements OnInit /*,OnChanges*/ {
  editorConfig = { //tinyMCE not used
    readonly: false,
    plugins: 'autoresize lists table link image code codesample',
    toolbar: false,
    min_height: 100,
    max_height: 500,
    min_width: 300,
    max_width: 500,
    resize: false,
  }
  questions: QuestionDTO[] = [];
  taskForm: FormGroup;
  placeholder:string = "Option "
  disabled: boolean = true;
  @ViewChild('autosize') autosize: CdkTextareaAutosize | undefined;

  // dtos for making backend calls for creating a Question, Options, McQuestion and QuestionVersion
  questionData: QuestionDTO = {
    id: -1,
    name: '',
    description: '',
    // setting author based on some kind of user service?
    author: 1,
    score: 0,
    type: '',
    text: '',
  } as QuestionDTO;
  versionData: QuestionVersionDTO = {
        id: 1,
        version: 1,
        isApproved: false,
        successor: null,

  } as QuestionVersionDTO;

  mcQuestion: McQuestionDTO = {
    id: -1,
    questionId: -1,
    questionVersion: this.versionData,
    isSC: false,
    shuffleOptions: false,
  } as McQuestionDTO;

  mcQuestionOption: McQuestionOptionDTO = {} as McQuestionOptionDTO;

  questionId: number = 1;
  isSC: string | undefined;
  mcOptions: MCOptionDTO[] = [] as MCOptionDTO[];

  // for chatmodel interaction
  autogeneratedAnswers$?: Observable<McqGenerationDTO>;
  autogeneratedQuestionAndAnswers$?: Observable<McqGenerationDTO>;
  reevaluatedQuestionAndAnswers$!: Observable<{mcq: McqGenerationDTO, reasoning: string; }[]>; // not actually used yet
  evaluation$?: Observable<{ mcq: McqGenerationDTO, reasoning: string; }>; // not actually used yet
  mcDisabled: boolean = false;
  color: string = "warn"
  concepts: string[] = []
  conceptControl = new FormControl();
  filteredConcepts: Observable<string[]> | undefined;
  setScore: boolean = false;
  fetchingTitle: boolean = false;
  fetchingStates: boolean[] = [false, false, false, false, false, false];
  Questiontitle: string = "";
  immediateGeneration: boolean = false;
  constructor(
    private questionService : QuestionDataService,
    private dialog: MatDialog,
    private _snackBar: MatSnackBar,
    private mcqService: McqcreationService,
    private contentService: ContentService) {
    this.taskForm = new FormGroup({
      'title': new FormControl(null, Validators.required),
      'numberOfOptions': new FormControl(4, [Validators.required]),
      'description': new FormControl(null),
      'score': new FormControl(null, Validators.required),
      'type': new FormControl('', Validators.required),
      'options': new FormArray([],[Validators.required, Validators.min(2)]),
      'conceptName': new FormControl('', Validators.required),
    });
  }

  ngOnInit(): void {
    this.updateOptions(this.taskForm.get('numberOfOptions')?.value);
    this.onChanges();
    this.contentService.fetchAllConceptNames().subscribe(concepts => {
      concepts.forEach(concept => {
        this.concepts.push(concept);
      });
    });
    this.filteredConcepts = (this.taskForm.get('conceptName'))?.valueChanges
      .pipe(
        startWith(''),
        map(value => this.filter(value))
      )

    console.log("concepts: ",this.filteredConcepts)
  }

  private filter(value: string): string[] {
    const filterValue = value.toLowerCase();
    if(!filterValue){
      return this.concepts;
    }
    return this.concepts.filter(concept => concept.toLowerCase().includes(filterValue));
  }


  //Method to be called when values will change

  onChanges(): void {
    this.Questiontitle = this.taskForm.get('title')?.value;
    this.taskForm.get('text')?.valueChanges.subscribe(val => {
      this.Questiontitle = val;
    });
    this.taskForm.get('numberOfOptions')?.valueChanges.subscribe((value) => {
      this.updateOptions(value);;
    });
    this.getOptions().valueChanges.subscribe((value) => {
      this.updateIsSC();
      this.countCorrectOptions();
    });

  }

  /**
  *
  * @param numberOfOptions
  * sets the amount of available input fields to numberOfOptions
  */
  updateOptions(numOfOptions: number) {

    if(this.taskForm.get('numberOfOptions')?.value < 0) {
      this.taskForm.get('numberOfOptions')?.setValue(0);
    }
   const options = this.taskForm.get('options') as FormArray;
    const numberOfOptions = this.taskForm.get('numberOfOptions')?.value;

    if(numberOfOptions < 0 || numOfOptions === null){
      this.taskForm.get('numberOfOptions')?.setValue(0);
      return;
    }
  // Überprüfen, ob numberOfOptions größer als 6 ist
    if (numberOfOptions > 6) {
      // Setzen Sie numberOfOptions auf 6, wenn es größer als 6 ist
      this.taskForm.get('numberOfOptions')?.setValue(6);
      // Zeigen Sie eine Warnmeldung an
      alert('Sie können nicht mehr als 6 Optionen hinzufügen');
      return;
    }

    while (options.length !== numberOfOptions) {
      if (options.length < numberOfOptions) {
        options.push(new FormGroup({
          text: new FormControl('', Validators.required),
          isCorrect: new FormControl(false, Validators.required)
        }));
      } else {
        options.removeAt(options.length - 1);
      }

  }


  }

  getOptions(): FormArray {
    return this.taskForm.get('options') as FormArray;
  }

  clearTitle() {
    this.taskForm.get('title')?.setValue('');

  }

  generateTitle() {
    if(!this.taskForm.get('conceptName')?.value) {
      this._snackBar.open('You need to enter a Concept', 'Close', {
        duration: 5000,
      });
    }
    console.log("title fired")
    console.log("concept", this.taskForm.get('conceptName')?.value)
    if(this.taskForm.get('conceptName')?.value) {
      console.log("title fired again: ")
      this.fetchingTitle = true;
      this.mcqService.getQuestionTitle(this.taskForm.get('conceptName')?.value).subscribe(
        (questionTitle: {question: string}) => {
          console.log("generated question from llm for the title: ",  questionTitle.question)
          this.taskForm.get('title')?.setValue(questionTitle.question);
          this.fetchingTitle = false;
        }
      )

    }
  }

  getButtonLabel(index: number) {
    const option = this.getOptions().at(index) as FormControl;
    return option.get('text')?.value ? 'Regenerate' : 'Generate Answer';
  }

  clearOption(index: number) {
    const option = this.getOptions().at(index) as FormControl;
    option.get('text')?.setValue('');
  }

  updateIsSC() {
    if(this.getOptions().controls.filter(control => control.get('isCorrect')?.value === true).length > 1) {
      this.mcDisabled = true;
      this.taskForm.get("type")?.setValue("MC");
    } else if (this.getOptions().controls.filter(control => control.get('isCorrect')?.value === true).length === 1) {
      this.mcDisabled = false;
      this.taskForm.get("type")?.setValue("SC");
    } else if (this.getOptions().controls.filter(control => control.get('isCorrect')?.value === true).length === 0) {
      this.mcDisabled = false;
      this.taskForm.get("type")?.setValue("");
    }
    //this.taskForm.get("type")?.setValue(numberOfCorrectOptions > 1 ? "MC" : "SC");
  }

  generate() {
    const title = this.taskForm.get('title')?.value
    const numberOfOptions = this.taskForm.get('numberOfOptions')?.value
    if(title && numberOfOptions) {
      this.generateOptions()
    } else {
      if(!title && numberOfOptions) {
        this.fetchingTitle = true;
        this.generateQuestionAndOptions();
      }
    }
  }

  generateQuestionAndOptions() {
    const numberOfOptions = this.taskForm.get('numberOfOptions')?.value
    // change to dynamically get concept from dropdown
    console.log(this.taskForm.get('conceptName')?.value)
    const concept = this.taskForm.get('conceptName')?.value
    if(concept && numberOfOptions) {
      for(let i = 0; i < this.getOptions().length; i++) {
        const option = (this.getOptions().at(i) as FormGroup);
        this.fetchingStates[i] = !option.get('text')?.value;
      }
      this.mcqService.getQuestionAndAnswers(concept, numberOfOptions).subscribe(
        (questionAndAnswers: McqGenerationDTO) => {
          this.taskForm.get('title')?.setValue(questionAndAnswers.question);
          this.autogeneratedQuestionAndAnswers$ = of(questionAndAnswers);
          this.autogeneratedQuestionAndAnswers$.subscribe( answers => {
            console.log("answers: ",answers)
            this.taskForm.get('description')?.setValue(questionAndAnswers.description);
            this.taskForm.get('score')?.setValue(questionAndAnswers.score);

            for (let i = 0; i < this.getOptions().length; i++) {
              const option = (this.getOptions().at(i) as FormGroup);
              option.get('text')?.setValue(answers.answers?.[i].answer);
              option.get('isCorrect')?.setValue(answers.answers?.[i].correct);

              this.fetchingStates[i] = false;
            }
            this.fetchingTitle = false;

          })
        })

    } else {
      alert("Please enter a concept and the number of options")
    }
  }

  generateOptions(){
    const title = this.taskForm.get('title')?.value
    let emptyOptions = 0;
    const concept = this.taskForm.get('conceptName')?.value
    for(let i = 0; i < this.getOptions().length; i++) {
      const option = (this.getOptions().at(i) as FormGroup);
      if(!option.get('text')?.value) {
        emptyOptions++;
      }
    }
    if(title && concept) {
      for(let i = 0; i < this.getOptions().length; i++) {
        const option = (this.getOptions().at(i) as FormGroup);
        this.fetchingStates[i] = !option.get('text')?.value;
      }
      this.mcqService.getAnswers(title, emptyOptions, concept).subscribe(
        (answers: McqGenerationDTO) => {
          console.log("answers: ",answers)
          this.autogeneratedAnswers$ = of(answers);
          console.log("autogeneratedAnswers: ",this.autogeneratedAnswers$)
          this.autogeneratedAnswers$.subscribe( answers => {

            for (let i = 0; i < this.getOptions().length; i++) {
              const option = (this.getOptions().at(i) as FormGroup);
              console.log("answeroption: ",answers.answers?.[i].answer)
              option.get('text')?.setValue(answers.answers?.[i].answer);
              console.log("isCorrect: ",answers.answers?.[i].correct)
              option.get('isCorrect')?.setValue(answers.answers?.[i].correct);
              this.fetchingStates[i] = false;

            }
          })
        }
      )
    } else {
      alert("Please enter a Title, concept and the number of options")
    }
  }

  generateOption(index: number) {
    const option = (this.getOptions().at(index) as FormGroup).get('text')!;
    const isCorrect = (this.getOptions().at(index) as FormGroup).get('isCorrect')!;
    const opt = option!.value;
    const title = this.taskForm.get('title')?.value
    const concept = this.taskForm.get('conceptName')?.value
    let otherOpts: string[] = [];
    for (let i = 0; i < this.getOptions().length; i++) {
      if (i !== index) {
        const otherOption = (this.getOptions().at(i) as FormGroup).get('text');
        otherOpts.push(otherOption?.value);
      }
    }

    console.log("otherOptions: ",otherOpts);
    if(title) {
      option.setValue('');
      this.fetchingStates[index] = true;
      console.log("option before sending: ",opt)
      this.mcqService.getAnswer(title, opt, otherOpts, concept).subscribe(
        (answer: Answer) => {
          console.log("answer: ",answer)
          console.log("answer: ",answer.answer)
          console.log("isCorrect", answer.correct)
          option.setValue(answer.answer);
          isCorrect.setValue(answer.correct);
          this.fetchingStates[index] = false;
        }
      )
    }
  }


  checkDescription(): boolean {
    return this.taskForm.get('description')?.value != null;
  }

  checkScore(): boolean {
    // this.color = "primary";
    return this.taskForm.get("score")?.value != null;
  }

  clearAllFields() {
    this.taskForm.get('title')?.setValue('');
    this.taskForm.get('description')?.setValue('');
    this.taskForm.get('score')?.setValue('');
    this.taskForm.get('type')?.setValue('');
    this.taskForm.get('options')?.reset();
    this.taskForm.get('conceptName')?.setValue('');
  }


  reevaluate() {
    const title = this.taskForm.get('title')?.value
    const concept = "Anweisungen und Ausdrücke" //getConcept()
    let answers = ""
    for (let i = 0; i < this.getOptions().length; i++) {
      const otherOption = (this.getOptions().at(i) as FormGroup).get('text');
      answers = answers.concat(otherOption?.value + " ");
    }
    let emptyOptions = 0;
    for(let i = 0; i < this.getOptions().length; i++) {
      const option = (this.getOptions().at(i) as FormGroup);
      if(!option.get('text')?.value) {
        emptyOptions++;
      }
    }
    if(title) {
      for(let i = 0; i < this.getOptions().length; i++) {
        const option = (this.getOptions().at(i) as FormGroup);
        this.fetchingStates[i] = !option.get('text')?.value;
      }
      this.mcqService.getReevaluatedQuestionAndAnswers(title, emptyOptions, concept, answers).subscribe(
        (answers:{mcq: McqGenerationDTO, reasoning: string}) => {
          this.reevaluatedQuestionAndAnswers$ = of([answers]);
          console.log("autogeneratedAnswers: ",this.reevaluatedQuestionAndAnswers$)
          this.reevaluatedQuestionAndAnswers$.subscribe( answers => {
            let answerIndex = 0;
            for (let i = 0; i < this.getOptions().length; i++) {
              const option = (this.getOptions().at(i) as FormGroup);
              if(!option.get('text')?.value) {
                option.get('text')?.setValue(answers[answerIndex].mcq.answers?.[i].answer);
                option.get('isCorrect')?.setValue(answers[answerIndex].mcq.answers?.[i].correct);
                answerIndex++;
              }
              this.fetchingStates[i] = false;
            }
          })
        }
      )
    }
  }

  // method to open the dialogbox for adding score
  openDialog(): void {
    const dialogRef = this.dialog.open(ScoreComponent, {
      height: '270px',
      width: '270px',
      data: { score: this.taskForm.get('score')?.value }
    });

    dialogRef.afterClosed().subscribe(result => {
      console.log('The dialog was closed');
       this.taskForm.get('score')?.setValue(result);
       console.log("score: ",this.taskForm.get('score')?.value);


    });
  }
  // method to open the dialogbox for adding description
  openDescripDialog(): void {
    const dialogRef = this.dialog.open(DescriptionDialogComponent, {
      height: '320px',
      width: '320px',
      data: { description: this.taskForm.get('description')?.value }
    });

    dialogRef.afterClosed().subscribe(result => {
      console.log('The dialog was closed');
      this.taskForm.get('description')?.setValue(result);
      console.log("Description: ",this.taskForm.get('description')?.value);
    });
  }

  onCheckboxChange(event: MatCheckboxChange) {
    this.isSC = event.checked ? 'MC' : 'SC';

  }

  getCorrectOptionsCount() {
    return this.getOptions().value.map((option: { isCorrect: boolean; }) => option.isCorrect).filter((v: number) => v).length;
  }

  countCorrectOptions() {
    let correctOptions = 0;
    for (let option of this.mcOptions) {
      if (option.isCorrect) {
        correctOptions++;
      }
    }

    if(correctOptions >= 2) {
      this.mcDisabled = true;
    }
  }

  onSubmit(): void {

    if(this.taskForm.valid) {



      const formQuestionData: QuestionDTO = {
        author: 1, // need to set the author on some kind of user service
        description: this.taskForm.get('description')?.value,
        score: this.taskForm.get('score')?.value,
        type: this.taskForm.get('type')?.value,
        text: this.taskForm.get('title')?.value,// need to set the author on some kind of user service
        conceptNodeName: this.taskForm.get('conceptName')?.value,

        //conceptNode: this.taskForm.get('conceptNode or conceptElement')?.value, because of dropdown


      } as QuestionDTO;

      console.log("formData: ",formQuestionData);

      // setting the mcOptions from the options control array
      this.mcOptions = this.taskForm.get('options')?.value;

      // create question to get id and afterwards creating the (questionverion + options + McQuestion) entries (?)
      // make the other calls within the createquestion call? question -> questionversion -> mcquestion -> options (?)
      // ToDo: setting authorId

      // creating options can be done independently from the others based on the options control array
      this.questionService.createOptions(this.mcOptions).subscribe(options => {
        this.mcOptions = options;
        console.log("mcOptions after creating: ",this.mcOptions)
       });
      console.log("mcOptions: ",this.mcOptions);

      // creating question database entry first to get the questionId for following calls
      // using switchMap to concatenate the calls
      this.questionService.createQuestion(formQuestionData).pipe(

        // having version ready, we can create the mcquestion database entry (?)
        switchMap((questionResponse: QuestionDTO): Observable<McQuestionDTO> => {
          if(questionResponse.id) {
            this.mcQuestion.questionId = questionResponse.id;
            this.mcQuestion.shuffleOptions = true;
            return this.questionService.createMcQuestion(this.mcQuestion);
          } else {
            throw new Error("QuestionVersionId is undefined");
          }

        })
      )
      // creating the McQuestionOption entries for linking (?)
      .subscribe((mcQuestion: McQuestionDTO) => {
        console.log("mcQuestionOptionDTO: ",this.mcQuestionOption);
        console.log("mcQuestion: ",mcQuestion);
        this.mcOptions.forEach(option => {
          console.log("option: ",option);
          this.mcQuestionOption.mcQuestion = mcQuestion;
          this.mcQuestionOption.mcOption = option;
          this.questionService.createMcQuestionOption(this.mcQuestionOption).subscribe(

          );
        });
      });

      console.log("questionOptions again: ",this.mcQuestionOption);

      this.resetTaskForm();
      // Snackbar öffnen
      this._snackBar.open('Saved a new Question', 'Close', {
        duration: 5000,
      });
    }
  }

  resetTaskForm(): void {
    const questionValue = this.taskForm.get('title')?.value;
    this.taskForm.reset();
    this.taskForm.get('title')?.setValue(questionValue);
  }

  // NOT IMPLEMENTED: automatic evaluation of the question and its answer options
  evaluate() {
    const title = this.taskForm.get('title')?.value
    const numberOfOptions = this.taskForm.get('numberOfOptions')?.value
    const concept = "Anweisungen und Ausdrücke" //getConcept()
    let answers = ""
    for (let i = 0; i < this.getOptions().length; i++) {
      const otherOption = (this.getOptions().at(i) as FormGroup).get('text');
      answers = answers.concat(otherOption?.value + " ");
    }
    if(title && numberOfOptions) {
      this.mcqService.getEvaluation(title, numberOfOptions, concept, answers).subscribe(
        (answers: {mcq: McqGenerationDTO, reasoning: string}) => {
          this.evaluation$ = of(answers);
          console.log("autogeneratedAnswers: ",this.evaluation$)
          this.evaluation$.subscribe(answers => {
            console.log("evaluation answers: ",answers)
            for (let i = 0; i < this.getOptions().length; i++) {
              const option = (this.getOptions().at(i) as FormGroup);
              option.get('isCorrect')?.setValue(answers.mcq.answers?.[i].correct as boolean);
            }
          })
        }
      )
    }
  }

}


